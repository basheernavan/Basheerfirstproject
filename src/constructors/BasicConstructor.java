package constructors;

public class BasicConstructor {
	
	//create constructor, it is special type of method
	public BasicConstructor(){
		System.out.println("this is the basic constructor, it'll auto initiate ");
	}
	
	//normal void methods: 
	public void test1(){
		System.out.println("this method is executing from basic constructor class test1");
	}
	
	public void test2(){
		System.out.println("this method is executing from basic constructor class test2");
	}

}
