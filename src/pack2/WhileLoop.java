package pack2;
//Print 1 to 10 numbers using while loop?
public class WhileLoop {
	public static void main(String[] args) {
		int i=1; //initialization;
		while(i<=10) {//this is the condition, if the condition is true
			//it'll go to loop, and print the i value;
			System.out.println("print i value: "+i);
			i++;//incremental;
		}
	}

}
