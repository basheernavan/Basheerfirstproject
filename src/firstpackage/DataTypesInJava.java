package firstpackage;

public class DataTypesInJava {
	public static void main(String[] args) {
		//Boolean it can store only true or false;
		boolean b=true;
		boolean b1=false;
		System.out.println(b);
		System.out.println(b1);
		
		//char: it can store only one character, it is like one alphabet, or number, or special char
		char c='e';
		System.out.println(c);
		char c1='G';
		System.out.println(c1);
		char c2='3';
		char c3='@';
		char c4='$';
		System.out.println(c4);
		
		//String: it can store multiple chars, or group of words.		
		String s="selenium";
		System.out.println(s);
		
		String s1="the rain have been started";
		System.out.println(s1);
		
		//String is not a data type, string is a class, it can store multple characters or group of words,
		//it contains lot of mehtods, add, remove, equals, size,etc...
	}

}
