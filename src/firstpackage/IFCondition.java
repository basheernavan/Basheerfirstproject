//find out the greatest number out of two numbers?
public class IFCondition {
	public static void main(String[] args) {
		int a= 10;
		int b= 20;
		if(a>b){//a is comparing with b, if the condition is true it'll comes to if group,
			//if the condition is false it goes to else group.
			System.out.println("a is the lowest number: "+a);
		}else{
			System.out.println("b is the greatest number: "+b);
		}
		
	}

}
